                    <span v-else class="settings__section__content__row__subtitle">2FA is enabled.</span>
                    <div class="settings__section__content__row__actions">
                        <VButton
                            v-if="user.isMFAEnabled"
                            class="button"
                            font-size="14px"
                            width="208px"
                            label="Regenerate Recovery Codes"
