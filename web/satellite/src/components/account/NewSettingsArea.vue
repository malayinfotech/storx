                            :is-disabled="isLoading"
                        />
                        <VButton
                            class="button"
                            font-size="14px"
                            width="90px"
                            :is-white="user.isMFAEnabled"
                            :on-press="!user.isMFAEnabled ? enableMFA : toggleDisableMFAModal"
                            :label="!user.isMFAEnabled ? 'Enable 2FA' : 'Disable 2FA'"
                        />
                    </div>
                </div>
            </div>
        </div>
    </div>
